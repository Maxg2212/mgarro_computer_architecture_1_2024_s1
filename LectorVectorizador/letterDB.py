import turtle

def dibujar_letra_A(posx, posy):
    # Configuración inicial
    t = turtle.Turtle()
    t.speed(2)  # Velocidad del dibujo

    # Dibujar la letra A
    t.penup()
    print(turtle.window_height() / 2 - 50)
    t.goto(posx, posy)  # Posición inicial
    t.pendown()
    t.left(75)
    t.forward(100)
    t.right(150)
    t.forward(100)
    t.backward(50)
    t.right(105)
    t.forward(35)


